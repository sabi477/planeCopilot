import { createContext, useContext } from 'react'

// Basit iki dilli (TR/EN) altyapı. Standart havacılık terimleri (Flaps, Gear, V1,
// callout'lar, METAR, FMA modları) bilinçli olarak iki dilde de İngilizce kalır.
export const LangContext = createContext({ lang: 'tr', setLang: () => {} })

export function makeT(lang) {
  const t = (k) => (DICT[lang] && DICT[lang][k]) ?? DICT.en[k] ?? k
  const pick = (o) => (o && typeof o === 'object' && !Array.isArray(o) ? (o[lang] ?? o.en) : o)
  return { t, pick }
}

export function useLang() {
  const { lang, setLang } = useContext(LangContext)
  return { lang, setLang, ...makeT(lang) }
}

